// Sets duty cycle in terms of servo angles (300 degrees -> 100%)
// assume 3 degrees / pct
static void set_servo_angle(double angle) {
	return set_duty(angle / 3.00f);
}

// Sets duty cycle of PWM in %
static void set_duty(double pct) {
	if (pct < 0) pct = 0;
	else if (pct > 100) pct = 100;

	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint32_t)(pct * 99.99f)); // 9999/100%
}
